package liftechnology.com.mx.planalimenticiokmp.presentation.model

/**
 * Enum que representa los diferentes tipos de mensajes toast.
 *
 * @author Pelkidev
 * @version 1.0.0
 */
enum class ToastType {
    /** Tipo para mensajes de éxito. */
    SUCCESS,
    /** Tipo para mensajes de error. */
    ERROR,
    /** Tipo para mensajes de advertencia. */
    WARNING,
    /** Tipo para mensajes informativos. */
    INFORMATIVE
}